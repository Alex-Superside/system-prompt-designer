"""Agents responsible for creating, evaluating, and refining prompt designs.

The three agents are intentionally stateless beyond their injected collaborators:
all meaningful state travels as arguments and return values.  This makes each
agent independently testable and composable.

DesignAgent
-----------
Turns an AgentSpec into a PromptDesign.  When an LLMClient is provided, the
heuristic scaffold produced by the tier patterns is enriched with substantive
content generated by the model.  Without a client the system falls back to
heuristic-only output — no degradation of the interface.

EvaluatorAgent
--------------
Runs lightweight, rule-based checks against evaluation scenarios.  No LLM
required for the current implementation.

RefinementAgent
---------------
Suggests edits to a design based on an EvaluationReport.  Currently heuristic;
an LLM could be wired in later via the same LLMClient Protocol.
"""

