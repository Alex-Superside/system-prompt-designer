@dataclass
class DesignAgent:
    """Agent responsible for turning an AgentSpec into a PromptDesign.

    When ``llm_client`` is provided, ``create_design`` first builds a
    heuristic scaffold via the tier pattern, then calls the LLM to enrich the
    TaskBlock (goal, acceptance criteria, constraints) and — for Tier 2+ —
    replaces the generic execution steps with agent-specific ones.

    Without an LLM client the heuristic scaffold is returned unchanged.  The
    interface is identical in both modes; callers cannot tell the difference.

    The agent is stateless: no design state is kept on the instance.
    """

    pattern_selector: PatternSelector
    rule_block_registry: RuleBlockRegistry
    llm_client: LLMClient | None = None
    verbose: bool = False
    template_loader: Callable[[str], str] | None = None

    def create_design(
        self,
        agent_spec: AgentSpec,
        explicit_tier: str | None = None,
    ) -> PromptDesign:
        """Build a PromptDesign from *agent_spec*, optionally enriched by an LLM.

        Args:
            agent_spec: The agent specification to design for.
            explicit_tier: If given, bypasses auto-tier selection.

        Returns:
            A PromptDesign — heuristic-only when no LLM client is configured,
            LLM-enriched otherwise.
        """
        if explicit_tier is not None:
            tier = PromptTier(explicit_tier)
        else:
            tier = self.pattern_selector.choose_tier(agent_spec)

        pattern = self.pattern_selector.get_pattern(tier)
        design = pattern.build(agent_spec, self.rule_block_registry)

        if self.llm_client is None:
            return design

        return self._enrich_with_llm(design, tier)

    def _merge_enriched_into_design(
        self,
        design: PromptDesign,
        enriched_task: TaskBlock,
        suggested_steps: list[ExecutionStep],
        wants_steps: bool,
    ) -> PromptDesign:
        """Build a new PromptDesign with enriched task and optional new steps."""
        execution_plan = design.execution_plan
        if wants_steps and suggested_steps and execution_plan is not None:
            execution_plan = ExecutionPlanBlock(
                name=execution_plan.name,
                steps=suggested_steps,
            )
        return PromptDesign(
            agent_spec=design.agent_spec,
            tier=design.tier,
            task=enriched_task,
            execution_plan=execution_plan,
            rule_blocks=design.rule_blocks,
        )

    def _enrich_with_llm(self, design: PromptDesign, tier: PromptTier) -> PromptDesign:
        """Send the scaffold to the LLM and merge the enriched fields back.

        This method is intentionally separate from ``create_design`` so it can
        be tested in isolation and so the control flow in ``create_design``
        stays readable.

        Args:
            design: The heuristic scaffold to enrich.
            tier: The tier that was selected for this design.

        Returns:
            A new PromptDesign with the LLM-enriched TaskBlock (and, for Tier
            2+, LLM-suggested execution steps).  If the LLM call or parse
            fails, the original *design* is returned unmodified.
        """
        rule_block_names = [ref.name for ref in design.rule_blocks]
        wants_steps = tier in (PromptTier.TIER_2, PromptTier.TIER_3)
        prompt = _build_llm_prompt(
            design.agent_spec, tier, rule_block_names, self.template_loader
        )
        _print_verbose_prompt(self.verbose, "PROMPT SENT TO LLM", prompt)

        client = self.llm_client
        assert client is not None
        try:
            raw_response = client.generate(prompt)
        except RuntimeError as exc:
            logger.warning("LLM call failed; returning heuristic design. Reason: %s", exc)
            return design

        enriched_task, suggested_steps = _parse_llm_task_enrichment(
            raw_response,
            fallback_task=design.task,
            wants_steps=wants_steps,
        )
        return self._merge_enriched_into_design(
            design, enriched_task, suggested_steps, wants_steps
        )

    def refine_design(self, design: PromptDesign, feedback: str) -> PromptDesign:
        """Revise *design* using the LLM guided by *feedback*.

        The method builds a compact refinement prompt that shows the current
        design state alongside the feedback, then delegates parsing to the same
        ``_parse_llm_task_enrichment`` helper used by ``_enrich_with_llm``.
        This keeps the parsing logic in one place and ensures the refined
        design always has the same structure as any other PromptDesign.

        If no LLM client is configured, or if the LLM call or parse fails, the
        original design is returned unmodified so the caller always gets a
        valid result.

        Args:
            design: The current PromptDesign to revise.
            feedback: The user's plain-text feedback describing the change.

        Returns:
            A new PromptDesign with fields updated according to the feedback,
            or the original *design* when LLM enrichment cannot be performed.
        """
        if self.llm_client is None:
            logger.warning("No LLM client configured; returning original design unchanged.")
            return design

        wants_steps = design.tier in (PromptTier.TIER_2, PromptTier.TIER_3)
        prompt = _build_refinement_llm_prompt(design, feedback, self.template_loader)
        _print_verbose_prompt(self.verbose, "REFINEMENT PROMPT SENT TO LLM", prompt)

        client = self.llm_client
        assert client is not None
        try:
            raw_response = client.generate(prompt)
        except RuntimeError as exc:
            logger.warning("LLM refinement call failed; returning original design. Reason: %s", exc)
            return design

        enriched_task, suggested_steps = _parse_llm_task_enrichment(
            raw_response,
            fallback_task=design.task,
            wants_steps=wants_steps,
        )
        return self._merge_enriched_into_design(
            design, enriched_task, suggested_steps, wants_steps
        )


