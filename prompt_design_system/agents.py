# ---------------------------------------------------------------------------
# LLMClient Protocol
# ---------------------------------------------------------------------------


class LLMClient(Protocol):
    """Minimal interface for LLM providers used by this package.

    Any object with a matching ``generate`` signature satisfies this protocol.
    The ``model`` keyword argument is optional; providers may ignore it if they
    use a fixed model configured at construction time.
    """

    def generate(
        self, prompt: str, *, model: str | None = None
    ) -> str:  # pragma: no cover - interface only
        ...


# ---------------------------------------------------------------------------
# Prompt composition helpers (pure functions — no side effects)
# ---------------------------------------------------------------------------


def _print_verbose_prompt(verbose: bool, title: str, prompt: str) -> None:
    """Print prompt to stdout when verbose is True."""
    if not verbose:
        return
    print("\n" + "=" * 60)
    print(title)
    print("=" * 60)
    print(prompt)
    print("=" * 60 + "\n")


def _format_bullet_section(items: list[str], prefix: str = "  - ") -> str:
    """Format a list as bullet lines; return placeholder when empty."""
    return "\n".join(f"{prefix}{item}" for item in items) if items else "  (none)"


def _get_template(template_loader: Callable[[str], str] | None, name: str) -> str:
    """Return template content from loader or built-in default."""
    if template_loader is not None:
        return template_loader(name)
    return get_default_template(name)


def _build_llm_prompt(
    agent_spec: AgentSpec,
    tier: PromptTier,
    rule_block_names: list[str],
    template_loader: Callable[[str], str] | None = None,
) -> str:
    """Compose the prompt sent to the LLM for task enrichment.

    The prompt follows the orthogonal framework: it is explicit about WHAT it
    wants (Task section) versus HOW (step instructions) and references rule
    blocks by name only.  The LLM is asked to return structured JSON so the
    response can be parsed without fragile string splitting.

    Args:
        agent_spec: The agent specification being designed.
        tier: The selected prompt tier (affects whether execution steps are
            requested).
        rule_block_names: Names of the rule blocks attached to this design.
        template_loader: Optional loader for external template; uses built-in if None.

    Returns:
        A self-contained prompt string ready to be sent to an LLM.
    """
    template = _get_template(template_loader, DESIGN_ENRICHMENT_TEMPLATE_NAME)
    wants_steps = tier in (PromptTier.TIER_2, PromptTier.TIER_3)
    steps_instruction = (
        '\n- "execution_steps": list of 4-6 concise, agent-facing action strings '
        "that operationalise the task for this specific agent (not generic advice)."
        if wants_steps
        else ""
    )
    return template.format(
        role_description=agent_spec.role_description,
        summary=agent_spec.summary,
        tier_value=tier.value,
        inputs_section=_format_bullet_section(agent_spec.primary_inputs),
        outputs_section=_format_bullet_section(agent_spec.primary_outputs),
        constraints_section=_format_bullet_section(agent_spec.constraints),
        rule_block_section=_format_bullet_section(rule_block_names),
        steps_instruction=steps_instruction,
    ).strip()


def _build_refinement_llm_prompt(
    design: PromptDesign,
    feedback: str,
    template_loader: Callable[[str], str] | None = None,
) -> str:
    """Compose the prompt sent to the LLM for a feedback-driven refinement.

    The prompt presents the current design state and the user's feedback, then
    asks the LLM to produce a revised JSON object in the exact same schema as
    the original task enrichment response.  Keeping the schema identical means
    ``_parse_llm_task_enrichment`` can parse the response without any changes.

    Args:
        design: The current PromptDesign to refine.
        feedback: The user's plain-text feedback about what to change.
        template_loader: Optional loader for external template; uses built-in if None.

    Returns:
        A self-contained prompt string ready to be sent to an LLM.
    """
    template = _get_template(template_loader, REFINEMENT_TEMPLATE_NAME)
    wants_steps = design.tier in (PromptTier.TIER_2, PromptTier.TIER_3)
    steps_instruction = (
        '\n- "execution_steps": list of 4-6 concise, agent-facing action strings '
        "that operationalise the revised task."
        if wants_steps
        else ""
    )
    criteria_lines = _format_bullet_section(design.task.acceptance_criteria)
    constraint_lines = _format_bullet_section(design.task.constraints)
    steps_section = ""
    if wants_steps and design.execution_plan is not None:
        step_lines = "\n".join(
            f"  {s.order}. {s.description}"
            for s in sorted(design.execution_plan.steps, key=lambda s: s.order)
        )
        steps_section = f"\nExecution Steps:\n{step_lines}"
    return template.format(
        goal=design.task.goal,
        criteria_lines=criteria_lines,
        constraint_lines=constraint_lines,
        steps_section=steps_section,
        feedback=feedback,
        steps_instruction=steps_instruction,
    ).strip()


def _strip_markdown_fence(raw: str) -> str:
    """Remove optional markdown code fence from LLM response."""
    text = raw.strip()
    if not text.startswith("```"):
        return text
    lines = text.splitlines()
    inner = [line for line in lines[1:] if not line.strip().startswith("```")]
    return "\n".join(inner).strip()


def _parse_task_from_dict(
    data: dict,
    fallback_task: TaskBlock,
    wants_steps: bool,
) -> tuple[TaskBlock, list[ExecutionStep]]:
    """Build TaskBlock and steps from parsed JSON dict; fall back on invalid data."""
    goal = data.get("goal")
    acceptance_criteria = data.get("acceptance_criteria")
    constraints = data.get("constraints")
    if not isinstance(goal, str) or not goal.strip():
        logger.warning("LLM response missing valid 'goal'; using heuristic task.")
        return fallback_task, []

    enriched_task = TaskBlock(
        name=fallback_task.name,
        goal=goal.strip(),
        acceptance_criteria=(
            [str(c) for c in acceptance_criteria]
            if isinstance(acceptance_criteria, list)
            else fallback_task.acceptance_criteria
        ),
        constraints=(
            [str(c) for c in constraints]
            if isinstance(constraints, list)
            else fallback_task.constraints
        ),
        output_format_description=fallback_task.output_format_description,
    )
    steps: list[ExecutionStep] = []
    if wants_steps:
        raw_steps = data.get("execution_steps")
        if isinstance(raw_steps, list):
            steps = [
                ExecutionStep(order=idx + 1, description=str(s).strip())
                for idx, s in enumerate(raw_steps)
                if str(s).strip()
            ]
    return enriched_task, steps


def _parse_llm_task_enrichment(
    raw: str,
    fallback_task: TaskBlock,
    wants_steps: bool,
) -> tuple[TaskBlock, list[ExecutionStep]]:
    """Parse the LLM's JSON response and return an enriched TaskBlock.

    Parsing is defensive: any structural mismatch or missing key causes a
    graceful fallback to the original heuristic values so the caller always
    receives a valid TaskBlock regardless of LLM behaviour.

    Args:
        raw: Raw string returned by the LLM.
        fallback_task: The heuristic TaskBlock to fall back to on parse error.
        wants_steps: Whether to extract ``execution_steps`` from the response.

    Returns:
        A tuple of (enriched TaskBlock, list of ExecutionStep).  The step list
        is empty when ``wants_steps`` is False or when the LLM omitted the key.
    """
    text = _strip_markdown_fence(raw)
    try:
        data = json.loads(text)
    except json.JSONDecodeError as exc:
        logger.warning("LLM response was not valid JSON; using heuristic task. Error: %s", exc)
        return fallback_task, []

    if not isinstance(data, dict):
        logger.warning("LLM response parsed but was not a JSON object; using heuristic task.")
        return fallback_task, []

    return _parse_task_from_dict(data, fallback_task, wants_steps)


