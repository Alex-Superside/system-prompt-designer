@dataclass
class EvaluatorAgent:
    """Agent that evaluates a PromptDesign against scenarios and rule blocks.

    This implementation performs lightweight, rule-based checks without
    invoking an LLM. It focuses on structural alignment between scenarios
    and the Task / constraints in the PromptDesign.
    """

    llm_client: LLMClient | None
    rule_block_registry: RuleBlockRegistry

    def evaluate(
        self,
        design: PromptDesign,
        scenarios: Sequence[EvaluationScenario],
    ) -> EvaluationReport:
        results: list[EvaluationResult] = []

        for scenario in scenarios:
            outcome, notes = self._evaluate_single(design, scenario)
            results.append(
                EvaluationResult(
                    scenario_id=scenario.identifier,
                    outcome=outcome,
                    notes=notes,
                ),
            )

        return EvaluationReport(
            design_identifier=design.agent_spec.identifier,
            results=results,
        )

    def _evaluate_single(
        self,
        design: PromptDesign,
        scenario: EvaluationScenario,
    ) -> tuple[EvaluationOutcome, str]:
        searchable_text = " ".join(
            [
                design.task.goal,
                " ".join(design.task.acceptance_criteria),
                " ".join(design.task.constraints),
            ],
        ).lower()

        missing: list[str] = []
        for expected in scenario.expected_properties:
            if expected.lower() not in searchable_text:
                missing.append(expected)

        if not scenario.expected_properties:
            return EvaluationOutcome.SKIPPED, "No expected properties defined for scenario."

        if missing:
            notes = f"Missing expected properties in design: {', '.join(missing)}"
            return EvaluationOutcome.FAIL, notes

        return EvaluationOutcome.PASS, "All expected properties appear in task or constraints."


