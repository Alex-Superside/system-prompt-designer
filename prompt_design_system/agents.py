def _build_audit_llm_prompt(
    design: PromptDesign,
    template_loader: Callable[[str], str] | None = None,
) -> str:
    """Compose the prompt sent to the LLM for a linguistic audit."""
    template = _get_template(template_loader, AUDIT_TEMPLATE_NAME)
    criteria_lines = _format_bullet_section(design.task.acceptance_criteria)
    constraint_lines = _format_bullet_section(design.task.constraints)
    steps_section = ""
    if design.execution_plan is not None:
        step_lines = "\n".join(
            f"  {s.order}. {s.description}"
            for s in sorted(design.execution_plan.steps, key=lambda s: s.order)
        )
        steps_section = f"\nExecution Plan (HOW):\n{step_lines}"
    rule_blocks_section = _format_bullet_section([ref.name for ref in design.rule_blocks])
    return template.format(
        goal=design.task.goal,
        criteria_lines=criteria_lines,
        constraint_lines=constraint_lines,
        steps_section=steps_section,
        rule_blocks_section=rule_blocks_section,
    ).strip()


def _parse_audit_findings(data: dict) -> list[AuditFinding]:
    """Extract list of AuditFinding from parsed JSON."""
    return [
        AuditFinding(
            category=f.get("category", "unknown"),
            location=f.get("location", "unknown"),
            evidence=f.get("evidence", ""),
            suggestion=f.get("suggestion", ""),
        )
        for f in data.get("findings", [])
    ]


def _parse_llm_audit_report(raw: str, design_id: str) -> LinguisticAuditReport:
    """Parse the LLM's JSON response into a LinguisticAuditReport."""
    text = _strip_markdown_fence(raw)
    try:
        data = json.loads(text)
    except json.JSONDecodeError as exc:
        logger.warning("LLM audit failed to return valid JSON. Error: %s", exc)
        return LinguisticAuditReport(
            design_identifier=design_id,
            density_score=1,
            orthogonality_score=1,
            summary="Audit failed: LLM response was not valid JSON.",
        )

    findings = _parse_audit_findings(data)
    return LinguisticAuditReport(
        design_identifier=design_id,
        density_score=data.get("density_score", 1),
        orthogonality_score=data.get("orthogonality_score", 1),
        findings=findings,
        summary=data.get("summary", "No summary provided."),
    )


@dataclass
class LinguisticAuditorAgent:
    """Agent that audits a PromptDesign for linguistic and structural debt.

    Like DesignAgent, this agent is stateless and relies on the LLM for its
    analysis of "semantic noise" and "orthogonality violations."
    """

    llm_client: LLMClient
    verbose: bool = False
    template_loader: Callable[[str], str] | None = None

    def audit_design(self, design: PromptDesign) -> LinguisticAuditReport:
        """Analyze *design* for linguistic technical debt and orthogonality leaks.

        Args:
            design: The PromptDesign to audit.

        Returns:
            A LinguisticAuditReport containing findings and scores.
        """
        prompt = _build_audit_llm_prompt(design, self.template_loader)
        _print_verbose_prompt(self.verbose, "AUDIT PROMPT SENT TO LLM", prompt)

        try:
            raw_response = self.llm_client.generate(prompt)
        except RuntimeError as exc:
            logger.warning("LLM audit call failed. Reason: %s", exc)
            return LinguisticAuditReport(
                design_identifier=design.agent_spec.identifier,
                density_score=1,
                orthogonality_score=1,
                summary=f"Audit failed: {exc}",
            )
        return _parse_llm_audit_report(raw_response, design.agent_spec.identifier)
