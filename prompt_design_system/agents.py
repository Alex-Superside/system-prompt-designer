@dataclass
class RefinementAgent:
    """Agent that suggests refinements to a PromptDesign based on evaluation.

    For now, this agent does not mutate designs. It reports which scenarios
    failed and leaves actual editing decisions to a human or higher-level
    tooling. This keeps PromptDesign as a stable, explicit artefact.
    """

    def propose_refinements(
        self,
        design: PromptDesign,
        report: EvaluationReport,
    ) -> str:
        failed = [result for result in report.results if result.outcome == EvaluationOutcome.FAIL]

        if not failed:
            return "No structural refinements suggested. All scenarios passed."

        lines: list[str] = [
            "Suggested refinements:",
            "",
        ]

        for result in failed:
            lines.append(f"- Scenario '{result.scenario_id}' failed: {result.notes}")

        lines.append("")
        lines.append(
            "Consider updating the Task acceptance criteria or constraints to explicitly cover the missing properties.",
        )

        return "\n".join(lines)


